#include "Bibliotecas.hh"

class Archivo {
private:
   FILE *p;

public:

   void error(){std::cout << "Error de lectura." << std::endl;}//puede tener control de errores dentro.

   

   Ventana buscarObj(Ventana obj);//Busca un obj. en el archivo donde se guarda los estados.

};
//////////////////////////////////////////////////////////////////////////////

Ventana Archivo::buscarObj(Ventana obj){

   Ventana aux;
   p=fopen ("EstadoObjetos.dat","rb");

   if(p==NULL){error();}

   while(fread (&aux,sizeof obj,1,p)==1){
      if(obj.getID() == aux.getID()){
         fclose (p);
         return aux;
      }
   }

   aux.setID(-1);
   return aux;
}
